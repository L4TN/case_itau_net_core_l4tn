-- ============================================
-- Verificação
-- ============================================
SELECT 'Movimentações' AS Tabela, COUNT(*) AS Total FROM Tb_Movimentacao_Fundo;
SELECT 'Posições' AS Tabela, COUNT(*) AS Total FROM Tb_Posicao_Fundo;
SELECT f.Cd_Fundo,
       (SELECT COUNT(*) FROM Tb_Movimentacao_Fundo m WHERE m.Id_Fundo = f.Id) AS Movimentacoes,
       (SELECT COUNT(*) FROM Tb_Posicao_Fundo p WHERE p.Id_Fundo = f.Id) AS Posicoes,
       (SELECT TOP 1 Vlr_Patrimonio FROM Tb_Posicao_Fundo p WHERE p.Id_Fundo = f.Id ORDER BY Dt_Posicao DESC) AS UltimoPatrimonio
FROM Tb_Fundo f ORDER BY f.Cd_Fundo;
